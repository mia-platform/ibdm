package functions

import (
	"strings"
)

func TrimSpace(s string) string {
	return strings.TrimSpace(s)
}

func ToUpper(s string) string {
	return strings.ToUpper(s)
}

func ToLower(s string) string {
	return strings.ToLower(s)
}
